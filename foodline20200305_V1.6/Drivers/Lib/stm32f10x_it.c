/*******************************************************************************
* Function Name  : USART2_IRQHandler
* Description    : This function handles USART2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART2_IRQHandler(void)  
{
    OS_IntEnter();
    if (USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
    {
        u8 v = (u8)(USART_ReceiveData(USART2));
		if(HEAD_SIGN_H == v && gRightSign.isHaveHeadH != TRUE)
		{
			gRightSign.isHaveHeadH = TRUE;
		}
		else if(HEAD_SIGN_L == v && gRightSign.isHaveHeadH && gRightSign.isHaveHeadL != TRUE)
		{
			gRightSign.isHaveHeadL = TRUE;
		}
		else if(gRightSign.isHaveHeadH && gRightSign.isHaveHeadL && DisplayUartLen == 0)
		{
			DisplayUartLen = v;
			DisplayUartIdx = 0;
            if(v >= WIRELESS_BUF_LEN)
            {
				gRightSign.isHaveHeadH = FALSE;
				gRightSign.isHaveHeadL = FALSE;
				DisplayUartIdx = 0;
				DisplayUartLen = 0;
            }
		}
		else if(DisplayUartLen > 0 && gRightSign.isHaveHeadH && gRightSign.isHaveHeadL)
		{
			DisplayUartRxBuf[DisplayUartIdx++] = v;
			if(DisplayUartIdx >= DisplayUartLen)
			{
                if (gDisplayMsgBodyLen[DisplayUartMsgs] == 0)
                {
                    INPUT_EVENT evt;
                    evt.Type = Input_PeripCtrl;
                    evt.Subtype = Perip_Display_Comm;
                    evt.Info.b[0] = DisplayUartLen;
                    evt.Info.b[1] = DisplayUartMsgs;
                    
                    if (OS_MsgBoxSend(gDisplayMsg_Queue, &evt, OS_NO_DELAY, FALSE) != OS_ERR_MBOX_FULL)
                    {
                        OS_MemCpy(&(gDisplayMsgBody[DisplayUartMsgs][0]), DisplayUartRxBuf, DisplayUartLen);
                        gDisplayMsgBodyLen[DisplayUartMsgs] = DisplayUartLen;
                        
                        DisplayUartMsgs ++;
                        if (DisplayUartMsgs >= DISPLAY_RCVBODY_COUNT)
                            DisplayUartMsgs = 0;
                    }
                }
				gRightSign.isHaveHeadH = FALSE;
				gRightSign.isHaveHeadL = FALSE;
				DisplayUartIdx = 0;
				DisplayUartLen = 0;
			}
		}
    }
	USART_ClearITPendingBit(USART2, USART_IT_RXNE);		
    OS_IntExit();		
}

